function 雁塔地宫:战斗胜利处理(id组,战斗类型)
	self.数据[id组[1]].层数=self.数据[id组[1]].层数+1
	local cs=self.数据[id组[1]].层数
	for n=1,#id组 do
		local id=id组[n]
		玩家数据[id].zhandou=0
		if self.数据[id] then
			self.数据[id].层数=cs
		else
			self.数据[id] ={层数=cs,重置=false,次数=0}
		end
		self.数据[id].层数=cs
		self.数据[id].次数=self.数据[id].次数+1
		local 等级=玩家数据[id].角色.等级
		local 经验=等级*2888+cs*3000
		local 银子=等级*355+cs*555
		local jiangli=self:取奖励等级(cs)
		玩家数据[id].角色:添加经验(经验*HDPZ[jiangli].经验,"雁塔地宫",1)
		玩家数据[id].角色:添加银子(银子*HDPZ[jiangli].银子,"雁塔地宫",1)
		if 取随机数()<=HDPZ[jiangli].爆率 then
			local 链接 = {提示=format("#S(雁塔地宫)#G/%s#Y少侠在雁塔地宫中不断探寻，并成功通过了#P/%s#Y层地宫守护兽的考验，神秘人将地宫中挖掘出的",玩家数据[id].角色.名称,cs),频道="hd",结尾="#Y奖励给他，真是令人羡慕#80#80#80"}
			local 名称,数量,参数=生成产出(产出物品计算(HDPZ[jiangli].ITEM),jiangli)
			if 数量== 9999 then --环
				玩家数据[id].道具:给予道具(id,nil,nil,nil,nil,nil,名称,nil,nil,nil,链接)
			else
				玩家数据[id].道具:给予超链接道具(id,名称,数量,参数,链接)
			end
		end
	end
	if self.ditu.层数[cs] then
	    地图处理类:跳转地图(id组[1],self.ditu.层数[cs],self.ditu.传送坐标[cs].x,self.ditu.传送坐标[cs].y)
	end
end

