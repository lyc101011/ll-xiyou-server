	else
		self.数据=table.loadstring(读入文件([[sql/雁塔地宫.txt]]))
	end

	self:加载数据()
	self:更新怪物()
end

function 雁塔地宫:每日下调()
	for k,v in pairs(self.数据) do
		v.层数=v.层数-10
		v.次数=0
		if v.层数<1 then
		   v.层数=1
		end
	end
	写出文件([[sql/雁塔地宫.txt]],table.tostring(self.数据))
end

function 雁塔地宫:每周归零()
	self.数据={}
	写出文件([[sql/雁塔地宫.txt]],table.tostring({}))
end

function 雁塔地宫:取地宫最低层数(队员)
	 local tab = {}
	 for n = 1, #队员 do
	 	if not self.数据[队员[n]] then
	 	    self.数据[队员[n]] ={层数=1,次数=0}
	 	end
		tab[n] = {cs =  self.数据[队员[n]].层数,id=队员[n]}
	 end
	 table.sort(tab, function(a, b) return a.cs < b.cs end)
	 return tab[1].cs,tab[1].id
end

