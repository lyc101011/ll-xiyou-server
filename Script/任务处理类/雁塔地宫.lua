function 雁塔地宫:更新怪物()
	for a=1,#self.取怪物 do
		for b=1,#self.取怪物[a] do
			local 前缀=self.取怪物[a][b].前缀
			local 后缀=self.取怪物[a][b].后缀
			local 模型=self.取怪物[a][b].模型
			local 名称=模型
			self.取怪物[a][b].额外面板百分比={气血=1,伤害=1,法伤=1,速度=1,固定伤害=1,治疗能力=1,封印命中等级=1,抵抗封印等级=1,受伤百分比=1,增伤百分比=1,递增伤害=1} --百分比
			if 前缀 then
				名称=前缀..名称
				if 前缀属性[前缀] then
					for k,v in pairs(前缀属性[前缀]) do
						self.取怪物[a][b].额外面板百分比[k]=1+v/100
					end
				end
			end
			if 后缀 then
				名称=名称..后缀
				if 后缀属性[后缀] then
					for k,v in pairs(后缀属性[后缀]) do
						self.取怪物[a][b].额外面板百分比[k]=1+v/100
					end
				end
			end
			self.取怪物[a][b].名称=名称
			self.取怪物[a][b].QT={}
			if self.染色[模型] and self.染色[模型][前缀]  then
				self.取怪物[a][b].QT=table.copy(self.染色[模型][前缀])  --{变异=nil , 饰品=true , 染色方案=107 , 染色组={[1]=1,[2]=1} }
				if self.取怪物[a][b].QT.AI then
					if 前缀~="疯癫的" and 后缀~="刺客" then
					         if self.取怪物[a][b].QT.AI=="物理" and 取随机数()<=20 then
						elseif self.取怪物[a][b].QT.AI=="法系" and 取随机数()<=25 then
							local sss={"地狱烈火","奔雷咒","水漫金山","泰山压顶"}
							self.取怪物[a][b].QT.主动技能={sss[取随机数(1,4)]}
						else
							self.取怪物[a][b].QT.门派=fenlei[self.取怪物[a][b].QT.AI][取随机数(1,#fenlei[self.取怪物[a][b].QT.AI])]
						end
					end
				end
			end
		end
	end
end

