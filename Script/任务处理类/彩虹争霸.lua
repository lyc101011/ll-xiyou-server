	elseif 取等级要求(id,40)==false then
		常规提示(id,"#Y/队伍中有成员等级不符合要求")
		return
	end

	local 队伍id = 玩家数据[id].队伍
	if 队伍id ~= 0 then
		local 所属阵营1 = "无"
		local 可传送 = true
		if self.数据[id]~=nil and self.数据[id].阵营 ~= nil then
			所属阵营1 = self.数据[id].阵营
		end
		for n=1,#队伍数据[队伍id].成员数据 do
			local 成员id=队伍数据[队伍id].成员数据[n]
			local 所属阵营2 = "无"
			if self.数据[成员id]~=nil and self.数据[成员id].阵营 ~= nil then
				所属阵营2 = self.数据[成员id].阵营
			end
			if 所属阵营1~=所属阵营2 then
				可传送 = false
				break
			end
		end
		if 可传送 then --这里是大家都没报名的时候
			if 所属阵营1=="无" then
				所属阵营1 = self:分配阵营(id)
				常规提示(id,"自动报名成功！")
			end
			self:跳转地图(id,所属阵营1,类型)
			self:任务刷新(id)
			for n=1,#队伍数据[队伍id].成员数据 do
				local 成员id=队伍数据[队伍id].成员数据[n]
				if 类型>self.数据[成员id].层数 then
					self.数据[成员id].层数 = 类型
				end
				地图处理类:系统更新称谓(成员id,self.数据[成员id].阵营) --稳定了，把跳转和任务刷新写道下边
			end
		else
			常规提示(id,"队伍中有玩家所属阵营不一致。")
			return
		end
	else
		local 所属阵营1 = "无"
		if self.数据[id]~=nil and self.数据[id].阵营 ~= nil then
			所属阵营1 = self.数据[id].阵营
		end
		if 所属阵营1=="无" then --这里是没报名的时候也能进场
			所属阵营1 = self:分配阵营(id)
			常规提示(id,"自动报名成功！")
		end
		if 类型>self.数据[id].层数 then
			self.数据[id].层数 = 类型
		end
		self:跳转地图(id,所属阵营1,类型)
		self:任务刷新(id)
		地图处理类:系统更新称谓(id,self.数据[id].阵营)
	end
end

