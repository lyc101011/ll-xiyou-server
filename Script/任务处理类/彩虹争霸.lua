function 彩虹争霸:任务刷新(id)
	local 地图 = 玩家数据[id].角色.地图数据.编号
	local 队伍id = 玩家数据[id].队伍
	local 新任务=false
	local rw1=玩家数据[id].角色:取任务(6666)
	if 队伍id~=0 then
		if rw1==0 then --队长没有任务的时候
			local 任务id,ZU=取唯一任务(6666,id)
			任务数据[任务id]={
				id=任务id,
				DWZ=ZU,
				销毁=true,
				起始=os.time(),
				玩家id={},
				队伍组={},
				结束=3600,
				类型=6666,
				击杀={红=false,橙=false,黄=false,绿=false,蓝=false,靛=false,紫=false},
				当前级别 = 1
			}
			任务数据[任务id].进程=self:取任务进程(地图)
			for n=1,#队伍数据[队伍id].成员数据 do
				local 成员id=队伍数据[队伍id].成员数据[n]
				玩家数据[成员id].角色:取消任务(玩家数据[成员id].角色:取任务(6666)) --不管有没有直接取消，在添加
				玩家数据[成员id].角色:添加任务(任务id)
			end
		else
			任务数据[rw1].进程=self:取任务进程(地图)
			for n=1,#队伍数据[队伍id].成员数据 do
				local 成员id=队伍数据[队伍id].成员数据[n]
				if 玩家数据[成员id].角色:取任务(6666)==0 then
					玩家数据[成员id].角色:添加任务(rw1)
				else
					local rw3=玩家数据[成员id].角色:取任务(6666)
					if rw3~=rw1 then
						玩家数据[成员id].角色:取消任务(rw3) --不能直接取消，不然会把角色任务清空
						玩家数据[成员id].角色:添加任务(rw1)
					end
				end
				玩家数据[成员id].角色:刷新任务跟踪()
			end
		end
	else
		if rw1==0 then
			local 任务id,ZU=取个人唯一任务(6666,id)
			任务数据[任务id]={
				id=任务id,
				DWZ=ZU,
				销毁=true,
				起始=os.time(),
				玩家id={},
				队伍组={},
				结束=3600,
				类型=6666,
				击杀={红=false,橙=false,黄=false,绿=false,蓝=false,靛=false,紫=false},
				当前级别 = 1
			}
			任务数据[任务id].进程=self:取任务进程(地图)
			玩家数据[id].角色:添加任务(任务id)
		else
			任务数据[rw1].进程=self:取任务进程(地图)
			玩家数据[id].角色:刷新任务跟踪()
		end
	end
end

function 彩虹争霸:分配阵营(id)
	local 队伍id = 玩家数据[id].队伍
	local zy = "虹光"
	if self.云影玩家 <= self.虹光玩家 then
		zy = "云影"
	end
	if 队伍id ~= 0 then
		for n=1,#队伍数据[队伍id].成员数据 do
			local 成员id=队伍数据[队伍id].成员数据[n]
			self.数据[成员id]={阵营=zy,层数=0}
			if zy == "云影" then
				self.云影成员[成员id]=1
				self.云影玩家 = self.云影玩家 + 1
			else
				self.虹光成员[成员id]=1
				self.虹光玩家 = self.虹光玩家 + 1
			end
		end
	else
		self.数据[id]={阵营=zy,层数=0}
		if zy == "云影" then
			self.云影成员[id]=1
			self.云影玩家 = self.云影玩家 + 1
		else
			self.虹光成员[id]=1
			self.虹光玩家 = self.虹光玩家 + 1
		end
	end
	return zy
end

