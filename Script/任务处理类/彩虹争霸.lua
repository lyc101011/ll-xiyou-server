	self.黄境数量 = 20
	self.绿境数量 = 20
	self.循环次数 = 2
	self.云影成员={}
	self.虹光成员={}
	self.云影玩家 = 0
	self.虹光玩家 = 0
	self.活动开关 = false
	self.活动时间=QJHDSJ["彩虹争霸"]
	self:加载公共数据()
end

function 彩虹争霸:活动定时器()
	if self.活动开关 then
		if self.开始Time-os.time()>0 then --开活动的时间内
			if self.闯关开关 then --打怪时间
				if os.time()>= 600+self.刷怪Time then
					self:刷出场景怪()
					self.刷怪Time=os.time()
				end
				if self.开始Time-os.time()<600 then --最后10分钟，关闭打怪 计算胜利
					self:计算胜利方()
					self.宝箱开关=true
					self.闯关开关=false --走下面
				end
			else
				if self.进场开关==false and self.进场Time-os.time()<0 then --开启进场
					self:开启彩虹进场()
					self.进场开关 = true
				elseif self.闯关开关==false and self.正式Time and self.正式Time-os.time()<0 then --正式开始 闯关开关为关闭状态
					self:正式开始比赛()
					self.正式Time=nil
					self.闯关开关 = true
				elseif self.宝箱开关 and self.开始Time-os.time()<300 then --最后5分钟 刷箱子
					if self.宝箱Time==nil then
						self.宝箱Time=os.time()
					end
					if os.time() >= 60+self.宝箱Time then --1分钟刷一次宝箱
						self:发放彩虹宝箱()
						self.宝箱Time=os.time()
					end
				end
			end
		else
			self:关闭活动()
		end
	else
		if self.活动时间.日期=="每天" then
			if self.活动时间.时==服务端参数.小时+0 and self.活动时间.分==服务端参数.分钟+0 and self.活动时间.秒<=服务端参数.秒+0  then
				self:开启报名()
			end
		else
			local zhouji=tonumber(os.date("%w", os.time()))
			if zhouji==self.活动时间.日期 then
				if self.活动时间.时==服务端参数.小时+0 and self.活动时间.分==服务端参数.分钟+0 and self.活动时间.秒<=服务端参数.秒+0  then
					self:开启报名()
				end
			elseif self.活动时间.日期=="周135" and (zhouji==1 or zhouji==3 or zhouji==5) then
				if self.活动时间.时==服务端参数.小时+0 and self.活动时间.分==服务端参数.分钟+0 and self.活动时间.秒<=服务端参数.秒+0  then
					self:开启报名()
				end
			elseif self.活动时间.日期=="周246" and (zhouji==2 or zhouji==4 or zhouji==6) then
				if self.活动时间.时==服务端参数.小时+0 and self.活动时间.分==服务端参数.分钟+0 and self.活动时间.秒<=服务端参数.秒+0  then
					self:开启报名()
				end
			end
		end
	end
end

