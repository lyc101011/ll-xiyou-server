		腰带名称= 装备处理类.打造物品[腰带序列][等级+1]
	end
	local 鞋子序列=23
	local 鞋子名称= 装备处理类.打造物品[鞋子序列][等级+1]
	local 临时道具 = 1
	装备处理类:生成固定属性装备(id,等级*10,武器序列,武器名称,"系统产出",nil,true,id,无级别)
