function 礼包奖励类:全套灵饰(id,等级,无级别,专用)
	for n=1,#随机灵饰 do
		local 名称=制造装备[随机灵饰[n]][等级]
		local 临时道具 = 玩家数据[id].道具:灵饰处理(id,名称,等级,3,随机灵饰[n])
		if 玩家数据[id].道具:给予道具(id,nil,nil,nil,nil,nil,临时道具) then
			常规提示(id,"#Y/你得到了#R/"..名称)
		end
	end
end

