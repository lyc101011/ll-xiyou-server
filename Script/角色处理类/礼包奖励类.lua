	local 项链名称= 装备处理类.打造物品[项链序列][等级+1][取随机数(1,2)]
	if type(装备处理类.打造物品[项链序列][等级+1])=="table" then
		项链名称= 装备处理类.打造物品[项链序列][等级+1][取随机数(1,2)]
